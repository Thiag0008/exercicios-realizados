public class exercicio_3 {
    
    
    public static void main(String[] args) {
        
        int soma = 0;
        
        
        for (int i = 1; i <= 10; i++) {
            soma += i; 
            System.out.println("Somando " + i + ": soma parcial = " + soma);
        }
        
        
        System.out.println("\nA soma dos números de 1 a 10 é: " + soma);
    }
}
    
    
    

